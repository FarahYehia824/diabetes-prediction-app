import streamlit as st
import pandas as pd
import joblib

# Load model and scaler
model = joblib.load("diabetes_model.pkl")
scaler = joblib.load("diabetes_scaler.pkl")

st.title("ðŸ©º Diabetes Prediction App")
st.write("Enter patient details to predict diabetes:")

# Input fields
pregnancies = st.number_input("Pregnancies", 0, 20, 1)
glucose = st.number_input("Glucose", 0, 200, 120)
blood_pressure = st.number_input("Blood Pressure", 0, 130, 70)
skin_thickness = st.number_input("Skin Thickness", 0, 99, 20)
insulin = st.number_input("Insulin", 0, 850, 80)
bmi = st.number_input("BMI", 0.0, 70.0, 25.0)
diabetes_pedigree = st.number_input("Diabetes Pedigree Function", 0.0, 2.5, 0.5)
age = st.number_input("Age", 18, 100, 30)

# Predict button
if st.button("Predict"):
    # Create dataframe
    input_data = pd.DataFrame([[pregnancies, glucose, blood_pressure,
                                skin_thickness, insulin, bmi,
                                diabetes_pedigree, age]],
                              columns=['Pregnancies','Glucose','BloodPressure',
                                       'SkinThickness','Insulin','BMI',
                                       'DiabetesPedigreeFunction','Age'])

    # Scale input
    input_scaled = scaler.transform(input_data)

    # Prediction
    prediction = model.predict(input_scaled)[0]
    probability = model.predict_proba(input_scaled)[0][1]

    result = "ðŸ”´ Diabetic" if prediction == 1 else "âœ… Not Diabetic"
    st.subheader(result)
    st.write(f"Risk Probability: {probability:.1%}")